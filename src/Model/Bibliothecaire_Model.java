package Model;

import java.io.Serializable;

public class Bibliothecaire_Model implements Serializable {

	private int id;
	private String nom,prenom;
	
	public Bibliothecaire_Model() {
		super();
	}

	public Bibliothecaire_Model(int id, String nom, String prenom) {
		super();
		this.id = id;
		this.nom = nom;
		this.prenom = prenom;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getNom() {
		return nom;
	}

	public void setNom(String nom) {
		this.nom = nom;
	}

	public String getPrenom() {
		return prenom;
	}

	public void setPrenom(String prenom) {
		this.prenom = prenom;
	}
	
	
	
	
}
